package Modelo;

public class OtrosColores {

    private String id;
    private String idProducto;
    private String foto ;
    private String fotob ;
    private String color;
    private int talleS;
    private int talleM;
    private int talleL;
    private int talleXL;

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getIdProducto() {
        return idProducto;
    }

    public void setIdProducto(String idProducto) {
        this.idProducto = idProducto;
    }

    public String getFoto() {
        return foto;
    }

    public void setFoto(String foto) {
        this.foto = foto;
    }
    public String getFotob() {
        return fotob;
    }

    public void setFotob(String fotob) {
        this.fotob = fotob;
    }

    public String getColor() {
        return color;
    }

    public void setColor(String color) {
        this.color = color;
    }

    public int getTalleS() {
        return talleS;
    }

    public void setTalleS(int talleS) {
        this.talleS = talleS;
    }

    public int getTalleM() {
        return talleM;
    }

    public void setTalleM(int talleM) {
        this.talleM = talleM;
    }

    public int getTalleL() {
        return talleL;
    }

    public void setTalleL(int talleL) {
        this.talleL = talleL;
    }

    public int getTalleXL() {
        return talleXL;
    }

    public void setTalleXL(int talleXL) {
        this.talleXL = talleXL;
    }

}
